export function useHanUi(app: App<Element>) {
  const plugins = [
    HInfiniteScroll,
    HLoading,
    HMessage,
    HMessageBox,
    HNotification
  ];
  components.forEach(component => {
    app.component(component.name, component);
  });

  plugins.forEach(plugin => {
    app.use(plugin);
  });
}
